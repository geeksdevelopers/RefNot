import UIKit

extension UIColor {
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 45.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var blueyGrey: UIColor {
        return UIColor(red: 136.0 / 255.0, green: 153.0 / 255.0, blue: 170.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var burntYellow: UIColor {
        return UIColor(red: 221.0 / 255.0, green: 136.0 / 255.0, blue: 17.0 / 255.0, alpha: 1.0)
    }
    
}
